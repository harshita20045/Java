class Pattern{
public static void main(String [] args){
       int i=1;
       
       while(i<=5){
int j=1;
           while(j<=i){
            System.out.print("*");
             j++;
           }
             System.out.println();
i++;
       }
 

  int k=1;
       
       while(k<=5){
int f=5;
           while(f>=k){
            System.out.print("*");
             f--;
           }
             System.out.println();
k++;
       }
}


}